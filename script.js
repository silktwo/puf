let currentLang = localStorage.getItem("puf-lang") || "uk";
const hashLine = window.location.hash.replace("#line-", "");
let currentLine = lineData.uk[hashLine] ? hashLine : "optic";

function t(key) {
  return translations[currentLang]?.[key] || translations.uk[key] || key;
}

function applyLanguage(lang) {
  currentLang = lang;
  localStorage.setItem("puf-lang", lang);
  document.documentElement.lang = lang === "uk" ? "uk" : "en";

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    node.textContent = t(node.dataset.i18n);
  });

  document.querySelectorAll("[data-lang]").forEach((button) => {
    button.setAttribute("aria-pressed", String(button.dataset.lang === lang));
  });

  updateRoute(document.querySelector(".route-tabs button.active")?.dataset.route || "support");
  updateLine(currentLine);
  updateBuilderSummary();
  scrambleHeadlines();
}

document.querySelectorAll("[data-lang]").forEach((button) => {
  button.addEventListener("click", () => applyLanguage(button.dataset.lang));
});

const revealItems = document.querySelectorAll("[data-reveal]");

if ("IntersectionObserver" in window) {
  const observer = new IntersectionObserver(
    (entries) => {
      entries.forEach((entry) => {
        if (!entry.isIntersecting) return;
        entry.target.classList.add("is-visible");
        observer.unobserve(entry.target);
      });
    },
    { threshold: 0.16 }
  );

  revealItems.forEach((item, index) => {
    item.style.transitionDelay = `${Math.min(index % 5, 4) * 70}ms`;
    observer.observe(item);
  });
} else {
  revealItems.forEach((item) => item.classList.add("is-visible"));
}

function updateRoute(route) {
  const activeRoute = routeData[currentLang]?.[route] || routeData.uk.support;
  const label = document.querySelector("#route-label");
  const title = document.querySelector("#route-title");
  const copy = document.querySelector("#route-copy");
  const link = document.querySelector("#route-link");

  if (!label || !title || !copy || !link) return;

  label.textContent = activeRoute.label;
  title.textContent = activeRoute.title;
  copy.textContent = activeRoute.copy;
  link.textContent = activeRoute.link;
  link.href = activeRoute.href;

  document.querySelectorAll("[data-route]").forEach((button) => {
    button.classList.toggle("active", button.dataset.route === route);
  });
}

document.querySelectorAll("[data-route]").forEach((button) => {
  button.addEventListener("click", () => updateRoute(button.dataset.route));
});

function updateLine(line) {
  currentLine = line;
  const activeLine = lineData[currentLang]?.[line] || lineData.uk.optic;
  const title = document.querySelector("#line-title");
  const copy = document.querySelector("#line-copy");

  if (title) title.textContent = activeLine[0];
  if (copy) copy.textContent = activeLine[1];

  document.querySelectorAll("[data-line]").forEach((button) => {
    button.classList.toggle("active", button.dataset.line === line);
  });

  updateBuilderSummary();
}

document.querySelectorAll("[data-line]").forEach((button) => {
  button.addEventListener("click", () => updateLine(button.dataset.line));
});

document.querySelectorAll(".option-row button").forEach((button) => {
  button.addEventListener("click", () => {
    const group = button.closest(".option-row");
    group?.querySelectorAll("button").forEach((option) => option.classList.remove("active"));
    button.classList.add("active");
    updateBuilderSummary();
  });
});

function updateBuilderSummary() {
  const summary = document.querySelector("#builder-summary");
  if (!summary) return;

  const line = lineData[currentLang]?.[currentLine]?.[0] || lineData.uk.optic[0];
  const activeOptions = Array.from(document.querySelectorAll(".option-row button.active")).map((button) => button.textContent.trim());
  summary.textContent = [line, ...activeOptions].join(" · ");
}

function scrambleHeadlines() {
  if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;

  const alphabet = "abcdefghijklmnopqrstuvwxyz···---__";

  document.querySelectorAll("[data-scramble]").forEach((node) => {
    const finalText = node.textContent;
    const chars = Array.from(finalText);
    const startIndex = Math.floor(chars.length * 0.5);
    const animatedIndices = chars
      .map((char, index) => ({ char, index }))
      .filter(({ char, index }) => index >= startIndex && ![" ", ".", ",", "/", "\n"].includes(char))
      .map(({ index }) => index);
    let frame = 0;
    const totalFrames = 32;
    window.clearInterval(Number(node.dataset.scrambleTimer || 0));
    node.style.minHeight = `${node.offsetHeight}px`;

    const timer = window.setInterval(() => {
      const resolvedCount = Math.floor((frame / totalFrames) * animatedIndices.length);
      const output = chars.map((char, index) => {
        if (char === " " || char === "." || char === "," || char === "/" || char === "\n") return char;
        if (index < startIndex) return finalText[index];
        const animatedPosition = animatedIndices.indexOf(index);
        if (animatedPosition > -1 && animatedPosition < resolvedCount) return finalText[index];
        return alphabet[Math.floor(Math.random() * alphabet.length)];
      }).join("");

      node.textContent = output;
      frame += 1;

      if (frame > totalFrames) {
        window.clearInterval(timer);
        node.textContent = finalText;
      }
    }, 36);

    node.dataset.scrambleTimer = String(timer);
  });
}

