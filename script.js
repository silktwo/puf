function updateBuilderSummary() {
  const summary = document.querySelector("#builder-summary");
  if (!summary) return;

  const line = lineData[currentLang]?.[currentLine]?.[0] || lineData.uk.optic[0];
  const activeOptions = Array.from(document.querySelectorAll(".option-row button.active")).map((button) => button.textContent.trim());
  summary.textContent = [line, ...activeOptions].join(" · ");
}

function scrambleHeadlines() {
  if (window.matchMedia("(prefers-reduced-motion: reduce)").matches) return;

  const alphabet = "abcdefghijklmnopqrstuvwxyz···---__";

  document.querySelectorAll("[data-scramble]").forEach((node) => {
    const finalText = node.textContent;
    const chars = Array.from(finalText);
    const startIndex = Math.floor(chars.length * 0.5);
    const animatedIndices = chars
      .map((char, index) => ({ char, index }))
      .filter(({ char, index }) => index >= startIndex && ![" ", ".", ",", "/", "\n"].includes(char))
      .map(({ index }) => index);
    let frame = 0;
    const totalFrames = 32;
    window.clearInterval(Number(node.dataset.scrambleTimer || 0));
    node.style.minHeight = `${node.offsetHeight}px`;

    const timer = window.setInterval(() => {
      const resolvedCount = Math.floor((frame / totalFrames) * animatedIndices.length);
      const output = chars.map((char, index) => {
        if (char === " " || char === "." || char === "," || char === "/" || char === "\n") return char;
        if (index < startIndex) return finalText[index];
        const animatedPosition = animatedIndices.indexOf(index);
        if (animatedPosition > -1 && animatedPosition < resolvedCount) return finalText[index];
        return alphabet[Math.floor(Math.random() * alphabet.length)];
      }).join("");

      node.textContent = output;
      frame += 1;

      if (frame > totalFrames) {
        window.clearInterval(timer);
        node.textContent = finalText;
      }
    }, 36);

    node.dataset.scrambleTimer = String(timer);
  });
}

